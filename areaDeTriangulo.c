#include <stdio.h>

int main() {

   //Declarando as variáveis
   float base, altura, area;

   //Base
   printf("Digite a base do triangulo: ");
   scanf("%f", &base);

   //Altura
   printf("Digite a altura do triangulo: ");
   scanf("%f", &altura);

   //Cálculo + resultado
   area = (base * altura) / 2;
   printf("Area do triangulo: %0.2f", area);

   //Retorno do programa = funcionou
   return 0;
}
